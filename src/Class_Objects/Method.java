package Class_Objects;

public class Method  {
	public int add(int n1, int n2)
	{
		int r = n1+n2;
		return r;
	}
	public int difference(int n1, int n2)
	{
		int r = n1 - n2;
		return r;
	}
	public int product(int n1, int n2)
	{
		int r = n1 * n2;
		return r;
	}
	public int divider(int n1, int n2 )
	{
		int r = n1/n2;
		return r;
	}
	public int remainder(int n1, int n2)
	{
		int r = n1%n2;
		return r;
	}
}

